<h4>d)	Payroll Accounting</h4>
<p>
Doing your own accounting and payroll is appealing to many small businesses until you've grown enough to benefit from outsourcing these services. Outsourced accounting and payroll can save you time and money through tax strategy, records to guide business decisions, and professionally-handled payroll.
</p>

<p>Here are the six steps to set up payroll accounting for your small business:</p>

<ol class="urlnav">
<li>Set up accounting software. Without decent accounting software, you will have to track all of your payments manually, which can be quite difficult. The right accounting software can make this much easier.</li>
<li>Create accounts for each expense type. As part of your accounting software setup, you’ll have to set up accounts for each expense that’s part of your payroll, including employee compensation, commissions, bonuses, payroll taxes, 401(k) matching and FICA withholding.</li>
<li>Set up recurring payments to employees. Once you set up your accounting software, you can program the software to issue regular payments to your employees.</li>
<li>Categorize payment portions. Break up your payments to employees into various categories for each covered expense (Medicare, taxes, etc.).</li>
<li>Process payments. As your employees complete their work, you’ll make regular payments to them, tracking all your business’s portions of those payments in your payroll accounting program.</li>
<li>Reconcile regularly. Even if you have payroll software that issues the payments to your employees and categorizes them automatically, it’s still important to check these payments routinely to ensure they are accurate and broken down correctly.</li>
</ol>

