							<h4>Audit</h4>

<p>The audit is unbiased and objective examination of the financial statements of an organization, irrespective of its size, nature and legal structure, with the aim of expressing the opinion on the financial statements through an audit report.
<br/>
The auditor evaluates whether the reports prepared by the entity conforms to the financial reporting framework, i.e. GAAP or IFRS. The primary objective of audit is to determine whether the financial statement represents true and fair view and the secondary objective is to detect if there are any errors or frauds, in the financial statements prepared by client.
<br/>
There can be two types of audits: Internal audit and external audit, wherein the internal audit is performed by the employees or head of the department of the organization, whereas the external audit is performed by external auditor.</p>

							<h4>Review</h4>

<p>A review provides limited assurance of financial statements, which is sufficient in many instances for a company and its creditors. Thus, it is a sort of mid-level assessment. In finer terms, it states that the financial statements are free from material misstatement.
<br/>
Our team performs all the required inquiry and analytical procedures to suggest if any modifications are required in the financial statements.</p>
								
								
